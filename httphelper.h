// Circle Buffer - Setup a simple circle buffer for streaming audio
const uint circle_size = 2001;
uint8_t circle_buffer[circle_size];
volatile int circle_in, circle_out;

// Circle Buffer - Function prototypes (so we don't need .h file, etc)
void circle_init();
bool circle_put(uint8_t);
bool circle_get(uint8_t *);

// Circle Buffer - Functions to do circle buffer
void circle_init(void) {
  circle_in = circle_out = 0;
}

bool circle_put(uint8_t n) {
  if (circle_in == ((circle_out - 1 + circle_size) % circle_size)) { return false; }  // Buffer full
  circle_buffer[circle_in] = n;
  circle_in = (circle_in + 1) % circle_size;
  return true;
}

bool circle_get(uint8_t *o) {
  if (circle_in == circle_out) { return false; }  // Buffer empty
  *o = circle_buffer[circle_out];
  circle_out = (circle_out + 1) % circle_size;
  return true;
}

// Thread Stuff - basically a parallel "loop" function.  This isn't called per-se.  It just exists and runs.
void PlayAudio_loop(void *pvParameters) {
  uint8_t x;
  for (;;) {
    if (circle_get(&x)) {
      sigmaDeltaWrite(4, x); //4 represents the audio channel.
      delayMicroseconds(125);
    } else {
      delay(10);  // If buffer is empty, waste time.
    }
  }
}

